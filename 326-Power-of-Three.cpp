/**
 * 完全不使用循环的解法:
 * 利用结论 "19是最大整型以内的最大的3的幂次"
 * 易证3^20 > 2^31
 * 只需证3^20 / 2^31 > 1
 * 即证(3/2)^20 / 2^11 > 1
 * 而(3/2)^20 = (9/4)^10 = (81/16)^5 = (5 + 1/16)^5 > 5^5 = 5 * 5^4 = 5 * 625 = 3125
 * 又知2^11 = 2048
 * 所以(3/2)^20 / 2^11 > 3125 / 2048 > 1
 * 直观推测3^19 / 2^31 = (3125/3) /2048 < 1
 */
bool isPowerOfThree(int n) {
      int M3=3*9*81*81*81*81; //3^19
      return n>0 && M3%n==0;
}
